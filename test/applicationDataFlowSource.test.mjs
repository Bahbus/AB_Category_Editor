test('category editor delegates Basics and description ownership through a narrow controller', () => {
  const categoryEditor = read('src/ui/categoryEditor.js');
  const basicEditor = read('src/ui/basicEditor.js');

  assert.match(categoryEditor, /import \{ renderBasicEditor \} from ['"]\.\/basicEditor\.js['"];/);
  assert.match(categoryEditor, /export \{ getBasicSwitchWarnings \} from ['"]\.\/basicEditor\.js['"];/);
  assert.match(categoryEditor, /const basicEditor = renderBasicEditor\(cat, \{/);
  assert.match(categoryEditor, /topEditorGrid\.append\(basicEditor\.card, colorCard\)/);
  assert.doesNotMatch(categoryEditor, /Basics<\/h3>|basic-switch-validation|description-generate-row|replaceGeneratedDescription|copyGeneratedDescription|numberInput\('Order'|textInput\('Name'|generateCategoryDescription|applyGeneratedDescriptionChange/);

  assert.match(basicEditor, /export function renderBasicEditor\(category, deps = \{\}\)/);
  assert.match(basicEditor, /return \{ card: basics, maybeAutoGenerateDescription, refreshValidation \};/);
  assert.doesNotMatch(basicEditor, /categoryEditor\.js|\.\.\/app\.js|getCategories|getSelectedIndex|renderAll|createScheduledRenderList|renderColorEditor|renderRangeStateFiltersEditor|renderMatchingRulesEditor/);

  const basicsIndex = categoryEditor.indexOf('basicEditor.card');
  const colorIndex = categoryEditor.indexOf('colorCard', basicsIndex);
  assert.ok(basicsIndex >= 0 && colorIndex > basicsIndex, 'Basics must precede Color in the top grid');

  const orderingStart = categoryEditor.indexOf('const itemOrdering = renderItemOrderingEditor');
  const orderingEnd = categoryEditor.indexOf('root.appendChild(itemOrdering)', orderingStart);
  const ordering = categoryEditor.slice(orderingStart, orderingEnd);
  assert.ok(ordering.indexOf('onValidationChanged: basicEditor.refreshValidation') < ordering.indexOf("onOrderingChanged: () => basicEditor.maybeAutoGenerateDescription('item ordering changed')"));

  const matchingStart = categoryEditor.indexOf('onRulesChanged: reason => {');
  const matchingEnd = categoryEditor.indexOf('},', matchingStart);
  const matching = categoryEditor.slice(matchingStart, matchingEnd);
  const matchingOrder = ['basicEditor.refreshValidation();', 'basicEditor.maybeAutoGenerateDescription(reason);', 'renderList();']
    .map(statement => matching.indexOf(statement));
  assert.ok(matchingOrder.every(index => index >= 0));
  assert.deepEqual(matchingOrder, [...matchingOrder].sort((a, b) => a - b));

  const filterStart = categoryEditor.indexOf('onFiltersChanged: reason => {');
  const filterEnd = categoryEditor.indexOf('},', filterStart);
  const filterCallback = categoryEditor.slice(filterStart, filterEnd);
  assert.ok(filterCallback.indexOf('updateCategoryIssueSummary();') < filterCallback.indexOf('basicEditor.maybeAutoGenerateDescription(reason);'));
});

test('range filter key references are imported or locally declared', () => {
  for (const file of sourceFiles('src')) {
    const source = read(file);
    if (!/\bRANGE_FILTER_KEYS\b/.test(source)) continue;
    const importsRangeKeys = /import \{[^}]*\bRANGE_FILTER_KEYS\b[^}]*\} from ['"][^'"]*constants\.js['"];/.test(source);
    const declaresRangeKeys = /\b(?:const|let|var)\s+RANGE_FILTER_KEYS\b/.test(source);
    assert.ok(importsRangeKeys || declaresRangeKeys, `${file} references RANGE_FILTER_KEYS without importing or declaring it`);
  }
});

