test('category editor delegates Range and State composition with narrow data flow and stable order', () => {
  const categoryEditor = read('src/ui/categoryEditor.js');
  const filterEditor = read('src/ui/rangeStateFiltersEditor.js');

  assert.ok(categoryEditor.includes("import { renderRangeStateFiltersEditor } from './rangeStateFiltersEditor.js';"));
  const delegationStart = categoryEditor.indexOf('const { ranges, states } = renderRangeStateFiltersEditor(rules, {');
  const delegationEnd = categoryEditor.indexOf('root.append(ranges, states);', delegationStart);
  const delegation = categoryEditor.slice(delegationStart, delegationEnd);
  assert.ok(delegationStart >= 0 && delegationEnd > delegationStart);
  assert.ok(delegation.includes('markDirty,'));
  assert.ok(delegation.includes('scheduleRenderList'));
  assert.ok(delegation.indexOf('updateCategoryIssueSummary();') < delegation.indexOf('maybeAutoGenerateDescription(reason);'));
  assert.doesNotMatch(categoryEditor, /rangeSliderControl|segmentedControl|RANGE_FILTER_NAMES|STATE_FILTER_NAMES|for \(const filter of RANGE_FILTERS\)|renderStateFilterCard/);

  assert.match(filterEditor, /export function renderRangeStateFiltersEditor\(rules, deps = \{\}\)/);
  assert.match(filterEditor, /const RANGE_FILTER_NAMES = Object\.fromEntries\(RANGE_FILTERS/);
  assert.match(filterEditor, /const STATE_FILTER_NAMES = Object\.fromEntries\(STATE_FILTERS/);
  assert.match(filterEditor, /rangeGrid\.className = 'grid cols-3 range-filter-grid'/);
  assert.match(filterEditor, /stateGrid\.className = 'grid cols-3'/);
  assert.doesNotMatch(filterEditor, /categoryEditor\.js|\.\.\/app\.js|getCategories|getSelectedIndex|renderAll|createScheduledRenderList/);

  const matchingRules = categoryEditor.indexOf('root.appendChild(renderMatchingRulesEditor');
  const filters = categoryEditor.indexOf('renderRangeStateFiltersEditor(rules');
  const advanced = categoryEditor.indexOf("const advanced = document.createElement('details')");
  assert.ok(matchingRules >= 0 && filters > matchingRules && advanced > filters);

  const rangeStateSchedule = categoryEditor.indexOf('const scheduleRenderList = createScheduledRenderList(renderList)');
  const colorSchedule = categoryEditor.indexOf('scheduleRenderList: createScheduledRenderList(renderList)');
  assert.ok(rangeStateSchedule >= 0 && colorSchedule > rangeStateSchedule, 'Range/State must share one scheduler distinct from Color');
});

